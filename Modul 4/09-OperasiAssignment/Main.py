# operasi yang dapat dilakukan dengan meringkas
# operasi ditambah dengan assignment 

a = 5 # assignment
print("nilai a = ", a)

a += 1 # artinya a = a + 1
print ("nilai a += 1, nilai a menjadi = ", a)

a -= 1 # artinya a = a - 1
print ("nilai -= 1, nilai a menjadi = ", a)

a *= 5 # artinya a = a * 5
print ("nilai *= 1, nilai a menjadi = ", a)

a /= 5 # artinya a = a / 5
print ("nilai /= 1, nilai a menjadi = ", a)

# modulus dan floor division
a = 2
a %= 5 # artinya a = a % 5
print ("nilai %= 5, nilai a menjadi = ", a)

a //= 5 # artinya a = a // 5
print ("nilai //= 5, nilai a menjadi = ", a)

# pangkat atau eksponen
a = 4
a **= 5 # artinya a = a ** 5 
print ("nilai **= 5, nilai a menjadi = ", a)

# OR
c = True
print ("nilai c = ", c)
c |= False
print ("nilai c |= false, menjadi c = ", c)
c |= True
print ("nilai c |= true, menjadi c = ", c)

# AND
c = True
print ("nilai c = ", c)
c &= False # artinys c = c and false 
print ("nilai c &= false, menjadi c = ", c)
