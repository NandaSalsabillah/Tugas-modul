# integer
# tipe data : bilangan bulat tanpa kaca 
data_integer = 11
print("data =" , data_integer)
print("tipe data", type (data_integer))

# FLOAT
# tipe data : bilangan desimal, dengan koma 
data_float = 3.14
print("data =", data_float)
print("tipe data", type (data_float))

# staring
# type data : kumpulan karakter 
data_staring = "Universitas Pendidikan Mandalika"
print("data =", data_staring)
print("tipe data", type (data_staring))

# boolean 
# tipe data : logika TRUE/FLASE
data_bool = True
print("data =", data_bool)
print("tipe data", type (data_bool))

